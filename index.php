                    password: "required",
                },
                messages: {
                    email_id: "Email ID",
                    password: "Password",
                },

                submitHandler: function(form) {
                    loginSubmit();
                }
            });

            var loginSubmit = function() {
                let token = '';
                var message = '';
                let post_data = {
                    email_id: $('[name=email_id]').val(),
                    password: $('[name=password]').val(),
                }
                loginAjaxCall(post_data);
            }

